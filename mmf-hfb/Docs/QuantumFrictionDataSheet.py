def get_Es_Ts(beta_K, beta_V, iState, V, g):
    res = data.query(f"beta_K=={beta_K} and beta_V=={beta_V}and g=={g} and iState== '{iState}'and V=='{V}'")
    Ts = res['wTime']
    E0 = res['E0']
    Ef = res['Ef']
    dE = (Ef- E0)/E0
    return dE, Ts

def plot_Es_Ts(beta_K, beta_V, g, V, iState, style=None):
    Es, Ts = get_Es_Ts(beta_K=beta_K, beta_V=beta_V, V=V, g=g, iState=iState)
    if Ts is None or len(Ts) ==0:
        return
    if style is None:
        plt.plot(Es, Ts, label=f"K={beta_K}, V={beta_V}, iState={iState}")
    elif style=='log':
        plt.loglog(Es, Ts, label=f"K={beta_K}, V={beta_V}, iState={iState}")
    elif style == 'semi':
        plt.semilogy(Es, Ts, label=f"K={beta_K}, V={beta_V}, iState={iState}")
    else:
        plt.plot(Es, Ts, label=f"K={beta_K}, V={beta_V}, iState={iState}")
    return Es, Ts


